#include "trap.h"    
.org 0x0
.text
.global _start
_start:
    # ==============================================
    # 第一组：地址/立即数加载指令 (lu12i.w、pcaddu12i)
    # 用 regdef 别名：t0=$r12, t1=$r13, t2=$r14
    # ==============================================
    lu12i.w     $t0, 0x700         # $t0 = 0x00700000（访存基址）
    lu12i.w     $t1, 0x100         # $t1 = 0x00100000（通用立即数加载）
    pcaddu12i   $t2, 0x200         # $t2 = PC + (0x200<<12)（PC相对加载）

    # ==============================================
    # 第二组：算术运算指令 (addi.w、add.w、sll.w)
    # 用 regdef 别名：a0=$r4, a1=$r5, a2=$r6, t8=$r20
    # ==============================================
    ori         $a0, $zero, 0x100   # $a0 = 0x100（运算基数1）
    ori         $a1, $zero, 0x200   # $a1 = 0x200（运算基数2）
    addi.w      $a2, $a0, 0x50      # $a2 = $a0+0x50 = 0x150（TEMU识别的addi.w）
    add.w       $a3, $a0, $a1        # $a3 = $a0+$a1 = 0x300（三寄存器加法）
    ori         $t8, $zero, 4       # $t8 = 4（移位位数，$t8=$r20）
    sll.w       $a4, $a1, $t8        # $a4 = $a1<<4 = 0x2000（三寄存器移位）

    # ==============================================
    # 第三组：逻辑运算指令 (or、ori、andi、xor)
    # 用 regdef 别名：x=$r21, fp=$r22, a5=$r9, a6=$r10, a7=$r11, s0=$r23
    # ==============================================
    ori         $x, $zero, 0x0F0F    # $x = 0x0F0F（运算数1）
    ori         $fp, $zero, 0x00FF   # $fp = 0x00FF（运算数2）
    or          $a5, $x, $fp         # $a5 = $x|$fp = 0x0FFF（三寄存器或）
    ori         $a6, $x, 0x000F      # $a6 = $x|0x000F = 0x0F0F（立即数或）
    andi        $a7, $x, 0x000F      # $a7 = $x&0x000F = 0x000F（立即数与）
    xor         $s0, $x, $fp          # $s0 = $x^$fp = 0x0F06（三寄存器异或）

    # ==============================================
    # 第四组：比较指令 (sltui) + 分支指令 (beq、bne、bgeu)
    # 用 regdef 别名：s1=$r24, s2=$r25, s3=$r26, s4=$r27
    # ==============================================
    ori         $s1, $zero, 0x500    # $s1 = 0x500（比较基数1）
    ori         $s2, $zero, 0x500    # $s2 = 0x500（比较基数2）
    ori         $s3, $zero, 0x300    # $s3 = 0x300（比较基数3）
    
    sltui       $s4, $s3, 0x600      # $s4 = (0x300<0x600) → 1（无符号比较）
    beq         $s1, $s2, label1     # $s1==$s2 → 跳转成功
    bne         $s1, $s3, label2     # $s1!=$s3 → 跳转成功
    bgeu        $s3, $s1, label3     # 0x300>=0x500 → 跳转失败

label1:
    ori         $s5, $zero, 0x1      # $s5=1（标记beq跳转成功）
    beq         $zero, $zero, label_end  # 无条件跳转（复用beq）
label2:
    ori         $s6, $zero, 0x2      # $s6=2（标记bne跳转成功）
    beq         $zero, $zero, label_end  # 无条件跳转
label3:
    ori         $s7, $zero, 0x3      # $s7=3（标记bgeu跳转成功，实际不执行）

label_end:
    # ==============================================
    # 第五组：访存指令 (st.w、ld.w、st.b、ld.b)
    # 用 regdef 别名：s4=$r27, s8=$r31, s7=$r30, t0=$r12
    # ==============================================
    lu12i.w     $s4, 0x1           # 第一步：$s4 = 0x1<<12 = 0x1000
    ori         $s4, $s4, 0x234      # 第二步：$s4 = 0x1000 | 0x234 = 0x1234（字写入值）
    ori         $s8, $zero, 0xCD     # $s8 = 0xCD（字节写入值）
    
    st.w        $s4, $t0, 0          # 字存储：0x00700000 ← 0x1234
    ld.w        $a3, $t0, 0          # 字加载：$a3 ← 0x00700000 → 0x1234
    st.b        $s8, $t0, 4          # 字节存储：0x00700004 ← 0xCD
    ld.b        $s7, $t0, 4          # 字节加载：$s7 ← 0x00700004 → 0xFFFFFFCD
    ld.b        $s8, $t0, 0          # 交叉验证：$s8 ← 0x00700000低8位 → 0x34

    # ==============================================
    # 收尾：触发陷阱，17条指令全执行
    # ==============================================
    HIT_GOOD_TRAP