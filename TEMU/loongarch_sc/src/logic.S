#include "trap.h"
.org 0x0
.text
.global _start

_start:
    # =========================================================
    # 斐波那契数列测试（fib测试）
    # 功能：计算斐波那契数列并写入数据存储器，验证读写一致性
    # 存储地址：0x80400000 ~ 0x80400100（共64个数据，字对齐）
    # 结果查看方式：
    # 1. 数据存储器 0x80400100 地址：0x55AA55AA = 测试完成；0x00000000 = 测试异常
    # 2. 寄存器 $t4（编号12）：0x00000064 = 测试完成；0x00000000 = 测试异常
    # 3. 正常完成自动退出，异常则无限循环等待调试
    # =========================================================
fib:
    addi.w      $t0, $zero, 0x1     # t0 = 1（斐波那契第1项）
    addi.w      $t1, $zero, 0x1     # t1 = 1（斐波那契第2项）
    lu12i.w     $a0, -0x7fc00        # a0 = 0x80400000（数据存储起始地址）
    addi.w      $a1, $a0, 0x100      # a1 = 0x80400100（数据存储结束地址）
    addi.w      $t4, $zero, 0x0     # t4 = 0（测试计数/状态标记）

loop0:
    add.w       $t2, $t0, $t1       # 计算下一项：t2 = t0 + t1
    addi.w      $t0, $t1, 0x0        # 更新前一项：t0 = t1
    addi.w      $t1, $t2, 0x0        # 更新当前项：t1 = t2
    st.w        $t2, $a0, 0x0        # 将当前项写入数据存储器
    ld.w        $t3, $a0, 0x0        # 从存储器读出验证
    bne         $t2, $t3, test_error # 读写不一致则标记异常
    nop
    addi.w      $a0, $a0, 0x4        # 地址偏移+4（32位字对齐）
    addi.w      $t4, $t4, 0x1        # 计数+1（记录已计算项数）
    bne         $a0, $a1, loop0      # 未到结束地址则继续循环
    nop

# 测试正常完成：标记结果并退出
test_ok:
    lu12i.w     $s0, -0x7fc00        # s0 = 0x80400000（数据存储起始地址）
    addi.w      $s0, $s0, 0x100      # s0 = 0x80400100（结果标记地址）
    li.w        $s1, 0x55AA55AA      # 测试完成标记值
    st.w        $s1, $s0, 0x0        # 写入标记到数据存储器
    li.w        $t4, 0x00000064      # t4 = 64（标记完成64项计算）
    HIT_GOOD_TRAP                    # 触发正常退出，解决无限循环问题
    nop

# 测试异常（读写不一致）：标记结果并循环等待调试
test_error:
    lu12i.w     $s0, -0x7fc00        # s0 = 0x80400000（数据存储起始地址）
    addi.w      $s0, $s0, 0x100      # s0 = 0x80400100（结果标记地址）
    li.w        $s1, 0x00000000      # 测试异常标记值
    st.w        $s1, $s0, 0x0        # 写入标记到数据存储器
    li.w        $t4, 0x00000000      # t4 = 0（标记异常）

end:
    beq         $zero, $zero, end    # 异常时无限循环，便于调试定位
    nop

# 无字符串常量（移除串口相关依赖）
.data
.align 2
# 仅保留数据存储器对齐，无额外数据